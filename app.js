function green() {
    let w = document.getElementById('dark')
    w.style.backgroundColor = '#23241c';
    w.style.color = '#949689';

    let x = document.getElementById('drop')
    x.style.display = 'none'


    let z = document.getElementById('text');
    z.style.color = 'green';
}

function drop() {
    let a = document.getElementById('drop');
    a.style.display = "block";
}
